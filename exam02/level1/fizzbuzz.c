#include <unistd.h>

void	ft_put_nbr(int i)
{

	if (i > 9)
		ft_put_nbr(i / 10);
	char c =  '0' + i % 10;
	write(1, &c, 1);
}

int	main()
{
	for (int i = 1 ; i <= 100 ; i++)
	{
		if (i % 3 == 0 || i % 5 == 0)
		{
			if (i % 3 == 0)
				write(1, "fizz", 4);
			if (i % 5 == 0)
				write(1, "buzz", 4);
		}
		else
			ft_put_nbr(i);
		write(1, "\n", 1);
	}
}
