#include <unistd.h>

int	main(int argc, char **argv)
{
	if (argc == 2)
	{
		for (int i = 0 ; argv[1][i] != '\0' ; i++)
		{
			char c = argv[1][i];
			if (c >= 'A' && c <= 'Z')
				c = 2 * 'A' + 25 - c;
			else if (c >= 'a' && c <= 'z')
				c = 2 * 'a' + 25 - c;
			write(1, &c, 1);
		}
	}
	write(1, "\n", 1);
	return 0;
}
