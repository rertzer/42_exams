#include <unistd.h>

int main(int argc, char **argv)
{
	if (argc == 2)
	{
		char *str = argv[1];
		while (*str)
		{
			char c = *str;
			if (c >= 'A' && c <= 'Z')
			{
				c = 2 * 'A' + 25 - c;
			}
			if (c >= 'a' && c <= 'z')
			{
				c = 2 * 'a' + 25 - c;
			}
			write(1, &c, 1);
			str++;
		}
	}
	write(1, "\n", 1);
	return 0;
}
