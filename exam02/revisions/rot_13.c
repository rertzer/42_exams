#include <unistd.h>

int	main(int argc, char **argv)
{
	if (argc == 2)
	{
		char *str = argv[1];
		while (*str)
		{
			char c = *str;
			if (c >= 'a' && c <= 'z')
				c = ((c - 'a') + 13) % 26 + 'a';
			if (c >= 'A' && c <= 'Z')
				c = ((c - 'A') + 13) % 26 + 'A';
			write(1, &c, 1);
			str++;
		}
	}
	write(1, "\n", 1);
	return 0;
}
